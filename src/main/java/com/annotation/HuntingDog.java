package com.annotation;

public class HuntingDog extends Animal{
    String type;

    public HuntingDog( String ration,  String color,  int weight,   String type) {
        super(ration, color, weight);
        this.type = type;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    @Override
    public String toString() {
        return "Animal [type=" + getType() + " ration=" + getRation() + ", color=" + getColor() + ", weight=" + getWeight() + "]";
    }
}
